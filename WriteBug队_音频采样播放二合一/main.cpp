int freq[] = {0, 0x3c, 0x3e, 0x40, 0x41, 0x43, 0x45, 0x47};

int get_freq(int x)
{
    if (x == 0)
        return (1 << 24) + 0x80;
    if (x >= 100)
    {
        x /= 100;
        if (x == 4)
            return 0x42;
        return 0x44;
    }
    if (x >= 10)
    {
        x /= 10;
        if (x == 1)
            return 0x48;
        return 0x4a;
    }
    if (x < 0)
    {
        if (x == -5)
            return 0x37;
        else if (x == -6)
            return 0x39;
        else
            return 0x3b;
    }
    return freq[x];
}

int get_delay(int d)
{
    if (d == 3)
    {
        return (base_delay >> 1) + (base_delay >> 2);
    }
    if (d < 0)
        return base_delay * (-d);
    return base_delay / d;
}

